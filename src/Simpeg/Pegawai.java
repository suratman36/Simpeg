    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        String tampilan = "dd-MM-yyyy";
            SimpleDateFormat fm = new SimpleDateFormat (tampilan);
            String tanggal1 = String.valueOf(fm.format(tgl.getDate()));
            
            // input data
        String sql = "insert into pegawai values (?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = con.prepareStatement(sql);
            stat.setString (1, a.getText());
            stat.setString (2, b.getText());
            stat.setString (3, tanggal1);
            stat.setString (4, buttonGroup1.getSelection().getActionCommand());
            stat.setString (5, (String)cbox.getSelectedItem());
            stat.setString (6, c.getText());
            stat.setString (7, d.getText());
            stat.setString (8, e.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            a.requestFocus();
            datatable();            
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" +e);
        }
    }//GEN-LAST:event_simpanActionPerformed

