    private void tblpegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpegawaiMouseClicked
        int bar = tblpegawai.getSelectedRow();
        
        a.setText (tabmode.getValueAt(bar, 0).toString());
        b.setText (tabmode.getValueAt(bar, 1).toString());
        try{
            DefaultTableModel tabmode = (DefaultTableModel)tblpegawai.getModel();
            Date date = new SimpleDateFormat ("dd-MM-yyyy").parse((String)tabmode.getValueAt(bar,2));
            tgl.setDate(date);
            } catch (ParseException ex){
            java.util.logging.Logger.getLogger(Pegawai.class.getName()).log(Level.SEVERE, null, ex);
        } 
          String jenis_kelamin = tabmode.getValueAt (bar,3).toString();
          if (jenis_kelamin.equals("Laki-laki")) {rb1.setSelected(true);}
          else {rb2.setSelected(true);}
          cbox.setSelectedItem (tabmode.getValueAt(bar, 4).toString());
          c.setText (tabmode.getValueAt(bar, 5).toString());
          d.setText (tabmode.getValueAt(bar, 6).toString());
          e.setText (tabmode.getValueAt(bar, 7).toString());
    }//GEN-LAST:event_tblpegawaiMouseClicked

